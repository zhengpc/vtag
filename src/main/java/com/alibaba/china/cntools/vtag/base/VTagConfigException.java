package com.alibaba.china.cntools.vtag.base;

/**
 * @author zhengpengcheng
 */
public class VTagConfigException extends RuntimeException {

    private static final long serialVersionUID = 41002216993617221L;

    public VTagConfigException() {
        super();
    }

    public VTagConfigException(String message, Throwable cause) {
        super(message, cause);
    }

    public VTagConfigException(String message) {
        super(message);
    }

    public VTagConfigException(Throwable cause) {
        super(cause);
    }

}
